package day20_forloop;

public class ForLoop1 {

	public static void main(String[] args) {

		//
		for (int i=0; i < 10 ; i++) {
			System.out.println("LOVE JAVA!");
		}
		System.out.println(); 
		
		
		for (int i=5; i<=15; i++) {
			System.out.println(i);
		}
		System.out.println();
		for (int num=20; num>=10; num--) {
			System.out.println(num);
		}
		System.out.println();
		
		
		
		
	}

}
